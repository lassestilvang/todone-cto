import type { Template, TemplateSectionData, TemplateTaskData, Priority } from '@/types';

const createSections = (names: string[]): TemplateSectionData[] =>
  names.map((name, order) => ({ name, order }));

type TaskInput = {
  content: string;
  description?: string;
  section: number;
  priority?: Priority;
  labels?: string[];
};

const createTasks = (items: TaskInput[]): TemplateTaskData[] =>
  items.map((item, order) => ({
    content: item.content,
    description: item.description,
    sectionIndex: item.section,
    priority: item.priority,
    labels: item.labels,
    order,
  }));

