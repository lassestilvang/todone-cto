import express from 'express';
import cors from 'cors';
import dotenv from 'dotenv';

dotenv.config();

const app = express();
const PORT = process.env.PORT || 4000;

app.use(cors());
app.use(express.json());

app.get('/health', (_req, res) => {
  res.json({ status: 'ok', message: 'Todone API is running' });
});

app.listen(PORT, () => {
  console.log(`Todone API server running on port ${PORT}`);
});

export default app;
